enum TimePeriod: String {
    case oneHour = "1h"
    case threeHour = "3h"
    case twelveHour = "12h"
    case oneDay = "24h"
    case sevenDay = "7d"
    case thirtyDay = "30d"
    case threeMonth = "3m"
    case oneYear = "1y"
    case threeYear = "3y"
    case fiveYear = "5y"
}
